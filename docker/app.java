public class SimpleMath 
{
    public static void main(String[] args) 
    {
        // Addition of 35 and 65
        int sum = 35 + 65;
        System.out.println("Sum of 35 and 65: " + sum);

        // Subtraction of 100 and 85
        int difference = 100 - 85;
        System.out.println("Difference of 100 and 85: " + difference);
    }
}
